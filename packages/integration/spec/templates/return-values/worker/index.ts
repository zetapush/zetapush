export default class Api {
  async returnsUndefined() {
    return undefined;
  }

  async returnsNothing() {}

  async returnsNull() {
    return null;
  }

  async returnsEmptyObject() {
    return {};
  }

  async returnsEmptyArray() {
    return [];
  }

  async returnsZero() {
    return 0;
  }

  async returnsEmptyString() {
    return '';
  }
}
